df["text"] = df[text_cols].agg(" ".join, axis=1).str.strip()

# ---------------------------------------------------
# 3️. EMBEDDING MODEL (VERY FAST)
# ---------------------------------------------------
model = SentenceTransformer("all-MiniLM-L6-v2")  # GPU-supported, super fast

print("Encoding Text...")
emb = model.encode(df["text"].tolist(), batch_size=32, show_progress_bar=True)

# ---------------------------------------------------
# 4️. CLUSTERING (Unsupervised Mental Health Topics)
# ---------------------------------------------------
k = 4  # (stress, anxiety, depression, neutral)
kmeans = KMeans(n_clusters=k, random_state=42, n_init='auto')
labels = kmeans.fit_predict(emb)

df["cluster"] = labels

# ---------------------------------------------------
# 5️. CLUSTER NAME MAPPING (auto-detect using keywords)
# ---------------------------------------------------
cluster_names = {}

keywords = {
    "stress": ["pressure", "tension", "overload", "burnout", "exhausted"],
    "anxiety": ["scared", "fear", "worry", "panic", "anxious"],
    "depression": ["sad", "alone", "hopeless", "empty", "cry"],
    "neutral": []
}

for c in range(k):
    sample_texts = " ".join(df[df["cluster"] == c]["text"].head(30)).lower()
    assigned = "neutral"
    for label, keys in keywords.items():
        if any(word in sample_texts for word in keys):
            assigned = label
            break
    cluster_names[c] = assigned

df["prediction"] = df["cluster"].map(cluster_names)

# ---------------------------------------------------
# 6️. SAVE RESULTS
# ---------------------------------------------------
df.to_csv("/content/drive/MyDrive/Colab Notebooks/dataset.csv", index=False)
