nn = NearestNeighbors(n_neighbors=6, metric="cosine", n_jobs=-1).fit(emb_reduced)

def recommend_similar(text: str, top_k: int = 5):
    """Return top_k similar posts from dataset for given input text"""
    text_clean = clean_text_series(pd.Series([text]))[0]
    emb = embedder.encode([text_clean], convert_to_numpy=True)
    emb_r = pca.transform(emb)
    dists, idxs = nn.kneighbors(emb_r, n_neighbors=top_k+1)  # +1 to skip itself if in DS
    idxs = idxs[0]
    dists = dists[0]
    # remove self if present (distance==0)
    results = []
    for dist, i in zip(dists, idxs):
        results.append({"index": int(i), "text": df.iloc[i][TEXT_COL], "pred_label": int(df.iloc[i][LABEL_COL]), "distance": float(dist)})
    return results[1:top_k+1] if len(results)>1 else results

