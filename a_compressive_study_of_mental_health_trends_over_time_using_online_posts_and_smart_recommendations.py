import pandas as pd
from sklearn.cluster import KMeans
from sentence_transformers import SentenceTransformer
import numpy as np

# ---------------------------------------------------
# 1️. LOAD DATA
# ---------------------------------------------------
df = pd.read_csv("/content/drive/MyDrive/Colab Notebooks/dataset.csv")   # <-- your uploaded file
